#include <iostream> 
int main(){
    int a=2;
    int b=5;
    // operators
    std::cout<<a+b<<"\n";
    std::cout<<a-b<<"\n";
    std::cout<<a*b<<"\n";
    std::cout<<a/b<<"\n";
    std::cout<<a%b<<"\n";
    // bodmas
    std::cout<<(2+3)*5<<"\n";
    // comparision operators
    std::cout<<(2>5)<<"\n";
    std::cout<<(5>2)<<"\n";
    std::cout<<(2==5)<<"\n";
    std::cout<<(2>=2)<<"\n";
    std::cout<<('a'!='A')<<"\n";
    return 0;
}
